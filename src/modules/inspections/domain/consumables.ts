/**
 * Расходники к заказу для данной модели и уровня ТО (накопительно).
 * Для ЕО и неизвестных моделей — пустой список.
 */
export function getConsumables(model: string | null | undefined, level: string): Consumable[] {
  const byLevel = model ? DATA[model] : undefined;
  if (!byLevel || !ORDER.includes(level as MaintLevel)) return [];
  const out: Consumable[] = [];
  for (const lv of ORDER) {
    const items = byLevel[lv];
    if (items) out.push(...items);
    if (lv === level) break;
  }
  return out;
}
