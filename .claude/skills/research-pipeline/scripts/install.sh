#!/usr/bin/env bash
# Phoenix Security — Research Pipeline Installer
set -e

echo ""
echo "╔══════════════════════════════════════════════════════╗"
echo "║   Phoenix Security — Research Pipeline Installer     ║"
echo "╚══════════════════════════════════════════════════════╝"
echo ""

# Python dependencies
echo "[1/4] Installing Python dependencies..."
pip install yt-dlp scrapling notebooklm-py notebooklm-cli praw requests \
            beautifulsoup4 rich curl-cffi browserforge --break-system-packages -q
echo "  ✓ Python packages installed"

# Scrapling playwright (optional, for JS-heavy pages)
echo ""
echo "[2/4] Scrapling stealth fetchers (optional)..."
python3 -c "import scrapling; scrapling.auto_install(True)" 2>/dev/null || \
  echo "  (stealth fetchers skipped — basic fetcher available)"

# Verify key binaries
echo ""
echo "[3/4] Verifying tools..."
python3 -c "import yt_dlp; print('  ✓ yt-dlp', yt_dlp.version.__version__)"
python3 -c "from scrapling import Fetcher; print('  ✓ scrapling')"
python3 -c "from notebooklm import NotebookLMClient; print('  ✓ notebooklm-py')"
/usr/local/bin/notebooklm --version 2>/dev/null && echo "  ✓ notebooklm-cli" || echo "  ⚠ notebooklm-cli not in PATH"

# Env setup — write .env with actual key
echo ""
echo "[4/4] Configuring API keys..."

SKILL_DIR="$(cd "$(dirname "$0")/.." && pwd)"
ENV_FILE="$SKILL_DIR/.env"
ENV_TEMPLATE="$SKILL_DIR/.env.template"

# Default Brave key — pre-filled at skill installation time.
# Override by setting BRAVE_API_KEY before running this script,
# or just edit .env after installation.
DEFAULT_BRAVE_KEY="${BRAVE_API_KEY:-YOUR_BRAVE_KEY_HERE}"

# Prompt interactively; pre-fill with the default so user can just hit Enter
if [ -t 0 ]; then
  # Interactive terminal — use read -e -i for pre-filled prompt
  echo "  Brave Search API key (press Enter to accept default):"
  read -e -i "$DEFAULT_BRAVE_KEY" -r ENTERED_BRAVE_KEY
  BRAVE_API_KEY="${ENTERED_BRAVE_KEY:-$DEFAULT_BRAVE_KEY}"
else
  # Non-interactive (piped/CI) — use default silently
  echo "  Non-interactive mode — using default Brave API key"
  BRAVE_API_KEY="$DEFAULT_BRAVE_KEY"
fi

# Write .env (live config, not template)
cat > "$ENV_FILE" << ENV
# Phoenix Security Research Pipeline — Environment Variables
# Auto-generated by install.sh — edit freely

# Brave Search API (primary web search)
# Get key at: https://api.search.brave.com/
BRAVE_API_KEY=${BRAVE_API_KEY}

# Reddit API (optional, for Reddit research)
# Create app at: https://www.reddit.com/prefs/apps
REDDIT_CLIENT_ID=
REDDIT_CLIENT_SECRET=

# NotebookLM default notebook (optional)
NOTEBOOKLM_NOTEBOOK_ID=
ENV

# Write .env.template as a clean reference (key blanked out)
cat > "$ENV_TEMPLATE" << 'ENV'
# Phoenix Security Research Pipeline — Environment Variables
# Copy to .env and fill in values

BRAVE_API_KEY=your_brave_api_key_here
REDDIT_CLIENT_ID=
REDDIT_CLIENT_SECRET=
NOTEBOOKLM_NOTEBOOK_ID=
ENV

echo "  ✓ .env written to: $ENV_FILE"
if [ "$BRAVE_API_KEY" = "YOUR_BRAVE_KEY_HERE" ]; then
  echo "  ⚠  Brave key is placeholder — edit .env or re-run with BRAVE_API_KEY=<key> ./install.sh"
else
  echo "  ✓ Brave API key configured"
fi

echo ""
echo "╔══════════════════════════════════════════════════════╗"
echo "║   Installation complete!                             ║"
echo "║                                                      ║"
echo "║   REQUIRED: Authenticate NotebookLM                 ║"
echo "║   Run in a SEPARATE terminal:                        ║"
echo "║     notebooklm login                                 ║"
echo "║                                                      ║"
echo "║   Then test the pipeline:                            ║"
echo "║     python3 scripts/research_pipeline.py             ║"
echo "╚══════════════════════════════════════════════════════╝"
echo ""
